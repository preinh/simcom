def add(a, b):
    """
    Add two numbers and return the return the result.

    :param a:
    The first number.
    :type a:
        `int`, `float`, `long`, or `complex` object.
    :param b:
        The second number.
    :type b:
        `int`, `float`, `long`, or `complex` object.
    """
    return a + b
